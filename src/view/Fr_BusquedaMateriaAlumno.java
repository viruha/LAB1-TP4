    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
        int legajo = Integer.parseInt(tf_Legajo.getText());
        for (Alumno alumno : Colegio.alumnos) {
            if (alumno.getLegajo() == legajo) {
                for (Materia materia : alumno.getListaMaterias()) {
                    jTextArea1.setText(jTextArea1.getText() + materia + "\n");
                }
            }
        }
        
        } catch(NumberFormatException nf) {
            JOptionPane.showMessageDialog(null, "Debe ingresar numero para el legajo");
        }
          
        
        
    }//GEN-LAST:event_jButton1ActionPerformed


